_PARSE_WORKFLOW_STEPS_SRC = """\
def parse_workflow_steps(workflow_output, max_steps=8):
    raw = str(workflow_output or "").strip()
    if not raw:
        return []
    steps = []
    for line in raw.split("\\n"):
        stripped = line.strip()
        if not stripped:
            continue
        candidate = stripped
        # Strip numbered prefix: "1. ", "1) ", etc.
        pos = 0
        while pos < len(candidate) and candidate[pos].isdigit():
            pos += 1
        if pos > 0 and pos < len(candidate) and candidate[pos] in ".)":
            candidate = candidate[pos + 1:].strip()
        # Strip bullet prefix
        elif candidate[:1] in "-*":
            candidate = candidate[1:].strip()
        if candidate:
            steps.append(candidate)
        if len(steps) >= max_steps:
            break
    return steps\
"""

