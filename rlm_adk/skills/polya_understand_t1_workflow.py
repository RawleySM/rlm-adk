_RUN_POLYA_UNDERSTAND_T1_WORKFLOW_SRC = '''\
def run_polya_understand_t1_workflow(
    objective,
    project_context,
    project_name="project",
    max_steps=8,
    max_chars_per_packet=12000,
    max_packets=10,
    max_retrievals=8,
    l2_threshold=4,
    emit_debug=True,
):
    """Run the T1 Workflow-First 3-Layer Polya understand topology.

    L0 generates workflow steps from the manifest.
    L1 assesses each step against context packets.
    L2 (conditional) does chunk-level assessment when packet count
    exceeds l2_threshold or L1 requests it.

    Args:
        objective: Concrete objective to validate.
        project_context: Context blob, shards, or manifest mapping.
        project_name: Friendly label for prompt context.
        max_steps: Maximum workflow steps L0 can generate.
        max_chars_per_packet: Packet size for chunking.
        max_packets: Maximum packet count after chunking/merging.
        max_retrievals: Maximum retrieval-order items.
        l2_threshold: Packet count threshold triggering L2.
        emit_debug: Whether to print debug logs.

    Returns:
        T1WorkflowResult with the synthesized understanding and
        ordered missing-context retrieval plan.
    """
    debug_log = []
    objective_text = (
        str(objective).strip()
        if str(objective).strip()
        else "Validate whether the current project context is sufficient to proceed."
    )

    def _log(msg):
        if emit_debug:
            print(msg)
        debug_log.append(msg)

    # --- Phase 0: Prepare context ---
    packets = prepare_context_packets(
        project_context,
        max_chars_per_packet=max_chars_per_packet,
        max_packets=max_packets,
    )
    manifest = build_context_manifest(project_context)
    _log(
        "[t1_workflow] Starting T1 workflow for "
        + project_name + " with " + str(len(packets)) + " packet(s)"
    )

    # --- Phase 1: L0 generates workflow steps ---
    _log("\\n[t1_workflow] Phase 1: L0 Workflow Generation")
    workflow_prompt = build_workflow_prompt(objective_text, manifest, max_steps)
    _log(
        "[t1_workflow] Dispatching workflow generation (prompt_len="
        + str(len(workflow_prompt)) + ")"
    )
    workflow_output = llm_query(workflow_prompt)
    workflow_steps = parse_workflow_steps(str(workflow_output), max_steps)
    _log(
        "[t1_workflow] Generated " + str(len(workflow_steps)) + " workflow step(s)"
    )
    if not workflow_steps:
        workflow_steps = ["Assess overall context sufficiency for: " + objective_text]
        _log("[t1_workflow] Fallback: using default single step")

    # --- Phase 2: L1 step assessment (batched) ---
    _log("\\n[t1_workflow] Phase 2: L1 Step Assessment")
    use_l2_flag = needs_l2_dispatch(packets, l2_threshold)
    assess_prompts = []
    for step_idx, step in enumerate(workflow_steps):
        assess_prompts.append(
            build_step_assessment_prompt(
                step, packets, manifest, step_idx, len(workflow_steps), use_l2_flag
            )
        )
    _log(
        "[t1_workflow] Dispatching " + str(len(assess_prompts))
        + " step assessor(s)"
    )
    assess_outputs = llm_query_batched(assess_prompts)
    step_assessments = []
    l2_requested_steps = []
    for step_idx, output in enumerate(assess_outputs):
        step_desc = workflow_steps[step_idx] if step_idx < len(workflow_steps) else ""
        assessment, wants_l2 = parse_step_assessment(str(output), step_desc)
        step_assessments.append(assessment)
        if wants_l2 or use_l2_flag:
            l2_requested_steps.append(step_idx)
    _log(
        "[t1_workflow] Received " + str(len(step_assessments))
        + " step assessment(s), " + str(len(l2_requested_steps))
        + " requesting L2"
    )

    # --- Phase 3 (conditional): L2 chunk assessment ---
    chunk_assessments_by_step = {}
    used_l2 = False
    if l2_requested_steps:
        _log("\\n[t1_workflow] Phase 3: L2 Chunk Assessment")
        used_l2 = True
        chunk_prompts = []
        chunk_prompt_keys = []  # (step_idx, chunk_idx) for reassembly
        for step_idx in l2_requested_steps:
            step = workflow_steps[step_idx] if step_idx < len(workflow_steps) else ""
            for chunk_idx, chunk in enumerate(packets):
                chunk_prompts.append(
                    build_chunk_assessment_prompt(
                        chunk, step, chunk_idx, len(packets)
                    )
                )
                chunk_prompt_keys.append((step_idx, chunk_idx))
        _log(
            "[t1_workflow] Dispatching " + str(len(chunk_prompts))
            + " chunk assessor(s)"
        )
        chunk_outputs = llm_query_batched(chunk_prompts)
        for key_idx, output in enumerate(chunk_outputs):
            step_idx, chunk_idx = chunk_prompt_keys[key_idx]
            chunk_id = "step_" + str(step_idx + 1) + "_chunk_" + str(chunk_idx + 1)
            ca = parse_chunk_assessment(str(output), chunk_id)
            chunk_assessments_by_step.setdefault(step_idx, []).append(ca)
            if step_idx < len(step_assessments):
                step_assessments[step_idx].chunk_assessments.append(ca)
        _log(
            "[t1_workflow] Received " + str(len(chunk_outputs))
            + " chunk assessment(s)"
        )
    else:
        _log("\\n[t1_workflow] Phase 3: L2 skipped (not needed)")

    # --- Phase 4: Synthesis ---
    _log("\\n[t1_workflow] Phase 4: Synthesis")
    synthesis_prompt = build_synthesis_prompt(
        objective_text,
        workflow_steps,
        step_assessments,
        chunk_assessments_by_step,
        manifest,
        used_l2,
    )
    _log(
        "[t1_workflow] Dispatching synthesis (prompt_len="
        + str(len(synthesis_prompt)) + ")"
    )
    synthesis_output = str(llm_query(synthesis_prompt))

    # Extract structured fields from synthesis (multi-line aware)
    def _extract_field(text, heading):
        heading_upper = heading.strip().upper()
        lines = text.split("\\n")
        for idx, line in enumerate(lines):
            stripped = line.strip()
            upper = stripped.upper()
            if upper.startswith(heading_upper + ":"):
                inline_value = stripped[len(heading_upper) + 1:].strip()
                section_lines = []
                for subsequent in lines[idx + 1:]:
                    sub_stripped = subsequent.strip()
                    if not sub_stripped:
                        if section_lines:
                            break
                        continue
                    if (
                        ":" in sub_stripped
                        and sub_stripped.split(":", 1)[0].replace("_", "").replace("-", "").replace(" ", "").isupper()
                        and len(sub_stripped.split(":", 1)[0].strip()) > 1
                    ):
                        break
                    section_lines.append(subsequent.rstrip())
                if section_lines:
                    if inline_value:
                        return inline_value + "\\n" + "\\n".join(section_lines)
                    return "\\n".join(section_lines)
                return inline_value
        return ""
    understanding = _extract_field(synthesis_output, "UNDERSTANDING")
    gap_assessment = _extract_field(synthesis_output, "GAP_ASSESSMENT")
    if not understanding:
        understanding = synthesis_output
    retrieval_order = extract_retrieval_order(synthesis_output, max_items=max_retrievals)

    _log(
        "[t1_workflow] Synthesis complete: "
        + str(len(retrieval_order)) + " retrieval candidate(s)"
    )

    return T1WorkflowResult(
        understanding=understanding,
        workflow_steps=workflow_steps,
        step_assessments=step_assessments,
        chunk_assessments=chunk_assessments_by_step,
        gap_assessment=gap_assessment,
        retrieval_order=retrieval_order,
        used_l2=used_l2,
        debug_log=debug_log,
    )\
'''

